using System;
using System.ComponentModel.DataAnnotations;

namespace asp_ecommerce.ViewModels
{
    public class RestoReviewViewModel
    {
        [Required]
        [MaxLength(256)]
        [Display(Name = "Write a review")]
        public string ReviewContent { get; set; }

        [Required]
        [Display(Name = "Resto name")]
        public string RestoName { get; set; }

        [Required]
        [DataType(DataType.Date, ErrorMessage = "Date is required!" )]
        [Display(Name = "Date visited")]
        public DateTime DateVisited { get; set; }
    }
}
